name = "shashank"
print(len(name))#finds length of the name
print(name.find("h"))#finds the place of the first letter
print(name.capitalize())#capitalizes the first character
print(name.upper())#makes all the characters upper case
print(name.lower())#makes all the characters in lower case
print(name.isdigit())#asking if the given value in variable is a digit
print(name.isalpha())#asking if the given value is alphabetical
print(name.count("h"))#counts the number of given value in a variable
print(name.replace("h","o"))#replaces the original value with the given value in a variable
print(name*3)#we can multiply strings by a int to print it multiple times
